let x = 10;
x++ // x++ là x+1, tăng giá trị của x lên 1

x+=5 // x+=5 là x=x+5, tăng giá trị của x lên 5

if (x > 10) {
    console.log('x lớn hơn 10');
}
if (x == 10) {
    console.log('x bằng 10');
}
else if (x===10) {
    console.log('x bằng 10 và kiểu dữ liệu là Number');
}
// == so sánh giá trị, === so sánh giá trị và kiểu dữ liệu